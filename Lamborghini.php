            <p> فروچیو لامبورگینی؛ بنیانگذار لامبورگینی، تحصیلاتش را در زمینه طراحی ماشین‌آلات کشاورزی و صنعتی، به پایان
              رساند، سپس وارد ارتش ایتالیا شد و در بخش نیروی هوایی مشغول به کار گردید. در جنگ جهانی دوم او به عنوان
              مکانیک، در یک کارخانه مشغول به کار شد و پس از جنگ، با استفاده از بقایای تجهیزات نظامی کارخانه موردنظر، به
              تولید تراکتور پرداخت؛ ولی او عاشق خودروهای سریع بود و همواره رؤیای سرعت، در سر می‌پروراند. وی در سال ۱۹۴۷
              با تقویت پیشرانه یکی از مدل‌های فیات، یک خودرو مسابقه‌ای فراهم کرد و در سال ۱۹۴۸ با این خودرو، در یک
              مسابقه محلی شرکت نمود. </p>
            <p>در ابتدای سال ۱۹۶۵، لامبورگینی به‌طور پیوسته تأکید می‌کرد که هرگز علاقه‌مند به انجام پروژه‌های مربوط به
              آینده یا عجیب، نشده‌است. فروچیو علاقه‌مند به طرح‌های مفهومی (نمایشی) نبود، او به سادگی می‌خواست که
              خودروهای عادی فوق‌العاده سریع و بی عیب و نقص را تولید کند. دو مهندس باهوش جوان از بولونیا، فرونچیو را درک
              کردند و بر روی یک طرح مفهومی در ذهن خود کار کردند. ایده این بود، که یک ماشین مسابقه‌ای کمی رام‌تر، برای
              جاده‌ها و قابل استفاده در حومه شهر، طراحی کنند.

              پروژه آن‌ها، به صورت مخفیانه، با نام تی‌پی ۴۰۰ و یک طراحی پیشرانه سطح متوسط، با پیشرانه ۱۲ سیلندر ۴ لیتری
              ۴۰۰جی‌تی که به صورت اریب در پشت کابین نصب شده بود، شروع شد. این مکان قرارگیری پیشرانه، از خصوصیات منحصر
              بفرد در این خودرو بود، زیرا تا آن زمان هیچ خودرو جاده‌ای اسپرت، پیشرانه خویش را در پشت کابین نصب نکرده
              بود. شاسی لامبورگینی میورا از ورق فلزی خم شده و جوش داده ساخته شده بود که باعث کاهش وزن بیشتر و افزایش
              عملکرد و قابلیت هندلینگ می‌شد.

              به منظور تأمین سابقه‌ای برای بسیاری از لامبورگینی‌های آینده، نام این مدل جدید میورا از گاوهای مسابقه‌ای
              اقتباس شد. گاوهای میورا ظاهراً از قوی‌ترین گاوهای نر مسابقه‌ای بودند و همچنین به عنوان باهوش‌ترین و
              درنده‌ترین آن‌ها نیز محسوب می‌شدند. وقتی به خودرو میورا از جلو با درب‌های که رو به بالا باز می‌شوند، نگاه
              کنیم، نام آن بسیار مناسب به نظر می‌رسد.

              دوباره با وجود زمان محدود و فشرده، بدنه خودروی میورا برای نمایش در نمایشگاه خودروی تورین در اکتبر ۱۹۶۵، به
              موقع تکمیل شد. این نمونه اولیه، یک هدف لحظه‌ای بود، اما منتقدان مخالف، به طراحی پیشرانه جلوی آن انتقاد
              کردند و متقاعد نشدند که میورا بتواند به عنوان یک ماشین جاده‌ای موفق عمل کند.

              نوچیو برتون و تیم طراحی او، طرح میورا را کمی عجیب تر از طرح‌های امروزی ساخت، اما مارچلو گاندینی، میورا را
              به یک ابرماشین جاده‌ای قابل بقا تبدیل کرد. سپس گاندینی اعلام کرد که از اکتبر ۱۹۶۵ تا فوریه ۱۹۶۶، همه
              کارکنان لامبورگینی در هفت روز هفته و ساعت‌ها به منظور آماده‌سازی میورای مناسب جاده برای نمایش در نمایشگاه
              خودرو ژنو و بلافاصله پس از آن برای تولید انبوه تلاش می‌کردند.

              نمونه اولیه میورا در اکتبر ۱۹۶۵ به عنوان یک شاسی هموار نمایش داده شده بود و تنها در چهار ماه تبدیل به
              نخستین ابرماشین جهان شد.
              &nbsp; </p>
            <p>پس از موفقیت میورا، این شرکت همچنان به آزمایش و تکامل راه خود ادامه داد. از سال ۱۹۶۷ تا ۱۹۷۱، لامبورگینی
              انواع مختلفی از خودروها را تجربه کرد اما طرح مفهومی مارزال، شاید یکی از عجیب‌ترین آن‌ها بود. با درهای
              گالوینگ، پیشرانه درون خطی ۶ سیلندر، فضای گستردهٔ شیشه‌ای و صندلی برای چهار نفر، این کانسپت ثابت، یک بستر
              آزمایشی برای مدل‌های آینده و به خصوص لامبورگینی اسپادا ۴ نفره ارائه کرد.

              نه به عنوان طراحی مورد علاقه همگان، اما اسپادا انواع شیشه را در طراحی خود دارا می‌باشد. این خودرو به‌طور
              نامتناسبی بزرگ است و دارای پایان عقب پیازی، کاپوت به همان اندازه بلند و شیشهٔ بیشتر است. مزیت مهم اسپادا
              در توان ۳۲۵ اسب بخار آن، پیشرانه جلوی V12 و کارایی چند وظیفه‌ای آن برای حمل چهار نفر بود.

              اسپادا، با تولید سه تیپ از آن در طول سال‌های ۱۹۶۸ تا ۱۹۷۸، تأکیدی بر موفقیت‌های لامبورگینی بوده‌است. شرکت
              فروچیو اکنون وارد دهه ۱۹۷۰ با خط تولید چشم‌گیری می‌شد. همراه با اسپادا، میورا اس جدید با پیشرانه جلوی
              جی‌تی مشابه مدل جی‌تی۳۵۰ با نام لامبورگینی ایسلرو ساخته شد. در کمتر از هشت سال، لامبورگینی به اوج موفقیت
              خود رسیده بود.</p>
            <P>در اوایل سال ۱۹۷۰، مشکلاتی از قبیل ناآرامی‌های کارگری در ایتالیا، افزایش قیمت گاز و معاملات بزرگ تراکتور،
              استرس فوق‌العاده‌ای را در لامبورگینی و شرکت او به وجود آورد؛ بنابراین در سال ۱۹۷۲ پس از هشت سال بودن در
              راس، فروچیو ۵۱٪ درصد از سهام شرکت را به یک کارخانه دار سوئیسی با نام ژرژ هنری روزتی فروخت.
              سال بعد او باقی سهام خود را نیز به دوست خود، رنه لایمری، واگذار کرد؛ بنابراین، مردی که نیروی محرکه صعود
              این شرکت بود، مجبور به حذف خود از این معامله شد.
              دهه ۱۹۷۰ زمان مناسبی برای خودروسازی‌ها نبود و شرکت لامبورگینی خود را برای سه بار بین سال‌های ۱۹۷۴ و ۱۹۷۸،
              در آستانه شکست دید تا در نهایت در سال ۱۹۷۸ به ورشکستگی رسید.</P>
            <P>در سال ۱۹۸۰، برادران میمران سوئیسی تصمیم به کمک به لامبورگینی گرفتند. در طول دهه ۱۹۸۰ تلاش‌های برادران
              میمران برای احیای این شرکت در نهایت با شکست مواجه شد و برادران میمران در اواسط دهه ۱۹۸۰ شروع به مذاکره با
              مدیران شرکت خودروسازی کرایسلر نمودند. این مذاکرات در نهایت در سال ۱۹۸۶ به پیوست شدن لامبورگینی به کرایسلر
              انجامید.</P>
