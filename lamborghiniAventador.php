                        <h4>شاسی لامبورگینی آونتادور
                        </h4>
                        <p>براى آنکه نیروى تولید شده توسط پیشرانه براحتى به وسیله جعبه دنده هفت‌سرعته اتوماتیک به چرخ‌ها
                            منتقل شود، آونتادور از یک سیستم چهارچرخ متحرک (AWD) بهینه‌سازى شده بهره مى‌گیرد. این سیستم
                            که به نام Haldex IV شناخته مى شود، به صورت هوشمندانه و بسته به شرایط مسیر و حالت حرکت خودرو،
                            نیروى تولیدى را بین چرخ هاى جلو و عقب تقسیم مى کند.سیستم دیفرانسیل خود قفل‌شونده عقب به
                            همراه دیفرانسیل جلو به صورت الکترونیکى توسط سیستم پایداری خودرو (ESP) کنترل مى شود تا بهترین
                            دینامیک و فرمان پذیرى براى خودرو به دست آید. البته راننده مى‌تواند از بین سه حالت ممکن شامل
                            Strada (یا جاده)، Sport و Corsa (یا پیست) یکى را انتخاب کند که وابسته به آن پیشرانه، جعبه
                            دنده، دیفرانسیل، فرمان و کنترل کننده‌هاى دینامیکی خودرو تغییر مى کند.سیستم تعلیق جناغى دوبل
                            آلومینیومى آونتادور نیز در نوع خود یکى از کم نظیرترین‌ها محسوب مى‌شود. لول فنرها و کمک فنرها
                            به صورت کامل از تکنولوژى خودروهاى فرمول وان اقتباس شده‌اند که در عین حال براى رانندگى شهرى
                            مورد بهینه‌سازى قرار گرفته‌اند.
                            <br>
                            براى متوقف کردن طبیعتا به دیسک ترمزهایى با قطر زیاد احتیاج است. آونتادور به صورت استاندارد
                            مجهز به دیسک هاى کربن/سرامیکى بسیار سبک شده که در جلو داراى قطرى برابر با ۴۰۰ میلى‌متر و در
                            عقب ۳۸۰ میلى‌متر هستند تا در ترکیب با کالیپرهاى شش پیستونه جلو و چهار پیستونه عقب، در هر
                            شرایطى خودرو را در کمترین مسافت ممکن متوقف کنند همچنین رینگ هاى آلیاژی ۱۹ اینچی که در محور
                            جلو با تایرهاى ۲۵۵ میلی‌متری و رینگ‌های ۲۰ اینچی محور عقب که با تایرهاى ۳۳۵ میلی‌متری پیرلى
                            از نوع Pzero روکش شده‌اند، بیشترین چسبندگى را فراهم مى‌کنند.
                        </p>

