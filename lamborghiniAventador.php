                        <h4>پیشرانه لامبورگینی آونتادور
                        </h4>
                        <p>اولین، خودروى تولید شده توسط فروچیو لامبورگینى در سال ۱۹۶۴ (یعنی GT350) مجهز به یک پیشرانه
                            ۳٬۵ لیترى ۱۲ سیلندر بود که توانایى تولید حداکثر قدرتى برابر با ۳۲۰ اسب‌بخار را داشت تا در
                            زمان خود یکى از پیشرفته ترین تکنولوژى پیشرانه را از آن خود کرده باشد. این پیشرانه در چندین
                            دهه مورد بهینه‌سازى و ارتقا قرار گرفت تا در مدل‌هاى میورا، کنتاچ، اسپادا، دیابلو و در نهایت
                            مورچیه‌لاگو مورد استفاده قرار گیرد اما مهندسان بخش تحقیق و توسعه (R&D) لامبورگینى براى ادامه
                            دادن این رویه پیشرانه ۱۲ سیلندر کاملا جدیدى را ساخته‌اند که نه تنها به واسطه تعداد سیلندرهاى
                            آن بلکه به خاطر تولید بیشترین نیروى ممکن در تمام دورهاى موتور و بواسطه تنفس طبیعى بودنش یکى
                            از خاص‌ترین نمونه‌هاى موجود در این صنعت به حساب مى آید.
                        </p>
                        </h4>
                        <div class="verticalhorizontal">
                            <img src="https://cargeek.live/images/aventador_TG1.jpg" alt="centered image" />
                        </div>
                        <p>نتیجه کار تیم R&D تولید پیشرانه ۱۲ سیلندر خورجینى جدید با زاویه رایج ۶۰درجه بین سیلندرها
                            گردید که با اندازه ۶۶٬۵ سانتى‌متری خود از بخش فوقانى تا پایین (به همراه عرض ۸۴۸ و طول ۷۸۴
                            میلى‌متر) به طور حتم از جمع‌وجورترین نمونه هاى ۱۲سیلندر به حساب مى آید. این پیشرانه وزنى در
                            حدود ۲۳۵ کیلوگرم دارد.پیشرانه به کار رفته در آونتادور (با کد L539) با حجم دقیقى معادل ۶٬۴۹۸
                            سى‌سى توانایى تولید حداکثر قدرت ۷۰۰ اسب بخار در ۸٬۲۵۰ دور در دقیقه و گشتاور عظیم ۶۹۰ نیوتن
                            متر را از دور ۵٬۵۰۰ دارد.با وجود چنین توانایى هایى میزان انتشار گازهاى آلاینده و همچنین مصرف
                            سوخت تا ۲۰ درصد کمتر از مدل مشابه قبلى شده است.مهندسان لامبورگینى براى استفاده بهینه از
                            اونتادور ، یک جعبه دنده فوق پیشرفته هفت سرعته ISR را براى این خودرو درنظر گرفته‌اند که فاصله
                            تعویض دنده‌اى برابر با ۵۰ میلى‌ثانیه دارد که در مقام مقایسه با جعبه دنده دوکلاچه آئودى R8 در
                            حدود دو برابر سریع‌تر است.
                        </p>
