                        <h4>نام لامبورگینی آونتادور
                        </h4>
                        <p>طبق سنت دیرین لامبورگینی هرکدام از خودروهاى تولیدى این شرکت براساس نام هاى گاوهاى اسپانیایى
                            نام‌گذارى مى شوند.
                            <br>
                            آونتادور نام یکى از شجاع‌ترین گاوهایى است که در اکتبر سال ۱۹۹۳ در شهر اراگزا اسپانیا با
                            مقاومت کم‌نظیرى در مقابل ماتادورها موفق به دریافت نشان شجاعت شده بود.
                        </p>
                        <div class="verticalhorizontal">
