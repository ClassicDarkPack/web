                        <h4>طراحی لامبورگینی آونتادور
                        </h4>
                        <p>«فلیپو پرینى» از مرکز استایل لامبورگینى زیر نظر طراح ارشد شرکت «مانفرد فیتزگرالد» خالق این
                            خودروی سوپر اسپرت است .
                            <br>
                            دماغه خودرو متشکل از خطوط شکسته و تیز که در ادامه با پروفیل بسیارکوتاه سقف و دیفیوزر غول
                            پیکر عقب تکمیل مى شود. اونتادور با طولى برابر با ۴۷۸۰ میلىمتر و پهناى بسیار زیاد ۲۲۶۰
                            میلى‌متر (شامل آیینه ها) در کنار ارتفاع ۱۱۳۶ میلى متر، بسیار بیش از آن چه واقعا هست عریض به
                            نظر میرسد که جدا از نسبت ارتفاع به پهناى کم، نباید از تاثیر ورودى هاى هواى بزرگ جلو نیز غافل
                            شد.
                        </p>
                        <div class="verticalhorizontal">
                            <img src="https://upload.wikimedia.org/wikipedia/commons/0/09/Lamborghini_Aventador_SVJ_NYIAS_2019.jpg"
                                alt="centered image" />

