            <p>
              استون مارتین در سال ۱۹۱۳ توسط لیونل مارتین و رابرت بمفورد تأسیس
              شد. این دو نفر با مشارکت یکدیگر شرکتی تحت عنوان بمفورد اند
              مارتین تأسیس کرده و اقدام به فروش ماشین‌هایی که توسط کمپانی
              سینگر از منطقه‌ای در کالو استریت، لندن نمودند. همچنین آنها در
              این منطقه به خودروهای تولید شده توسط کمپانی‌های جی.دبلیو.کی و
              کالتروپ خدمات تعمیر و نگهداری، ارائه می‌نمودند. مارتین در
              مسابقهٔ ویژه‌ای در آستون هیل، در منطقه‌ای نزدیک استون کلینتون
              شرکت کرد، که پس از موفقیت در آن، این دو نفر تصمیم به ساخت خودروی
              خود گرفتند. نخستین ماشینی که تحت عنوان استون مارتین تولید شد،
              توسط مارتین طراحی شده بود. این خودرو از قرار دادن یک موتور چهار
              سیلندر کاونتری سیمپلکس، بر روی اتاق یک ایزوتا فرانچینی مدل ۱۹۰۸
              تشکیل شده بود. این دو نفر مالکیت مناطقی را در هنیکر، واقع در
              کنزینگتون بدست آوردند و موفق شدند اولین اتومبیل مشترک خود را در
              مارس ۱۹۱۵ تولید کنند. اما به دلیل آغاز جنگ جهانی اول، فرایند
              ساخت این خودرو هرگز عملی نشد و با گسترش جنگ، مارتین به نیروی
              دریایی بریتانیا پیوست، بمفورد نیز به ارتش بریتانیا ملحق شد. تمام
              تجهیزات و ماشین‌آلات این کارخانه نیز به یک شرکت هواپیماسازی
              انگلیسی، تحت عنوان شرکت هوانوردی ساپ‌ویت فروخته شد. پس از جنگ،
              آنها محل جدیدی را در خیابان آبینگدن، کنزینگتون پیدا کردند و یک
              ماشین جدید طراحی نمودند. در سال ۱۹۲۰ بمفورد کارخانه را ترک نمود،
              ولی یک سال بعد، آنها با دریافت وام و تأمین بودجه لازم از کونت
              لوئیس زبوروسکی، شرکت بمفورد اند مارتین را احیا کردند. در سال
              ۱۹۲۲ شرکت بمفورد اند مارتین، تولید اتومبیل برای رقابت در مسابقات
              اتومبیل‌رانی جایزه بزرگ فرانسه را آغاز نمود. تا اواسط دهه ۱۹۲۰
              تقریباً ۵۵ اتومبیل، در دو پیکره شاسی بلند و شاسی کوتاه تولید شده
              بود. بامفورد اند مارتین در سال ۱۹۲۵ ورشکست شد و با تعطیلی
              کارخانه ساخت خودرو، مارتین شرکت را ترک نمود. در سال ۱۹۲۶ بیل
              رنویک و آگوستوس برتلی، با مشارکت چند سرمایه‌گذار همچون لیدی
              چارنوود کنترل شرکت را در اختیار گرفتند و اقدام به تغییر نام شرکت
              بمفورد اند مارتین تحت عنوان استون مارتین موتورز نمودند. در فاصله
              سالهای ۱۹۲۶ تا ۱۹۳۷ برتلی به عنوان مدیر فنی و طراح اتومبیل‌های
              آستون مارتین فعالیت می‌کرد. از سال ۱۹۳۶ استون مارتین بر ساخت و
              تولید خودروهای جاده‌ای تمرکز نمود و تا یک سال قبل از آغاز جنگ
              جهانی دوم شمار ۷۰۰ دستگاه خودرو تولید کرده بود. با آغاز جنگ دوم
              جهانی، استون مارتین ساخت خودرو را متوقف کرد. این شرکت در طول جنگ
              جهانی دوم به تولید قطعات هواپیما مشغول بود. از سال ۱۹۹۴ تا ۲۰۰۷
              استون مارتین بخشی از شرکت خودروسازی فورد بود، که در سال ۲۰۰۰ به
              عنوان بخشی از گروه اتوموتیو پرمیر محسوب شد. در ۱۲ مارس ۲۰۰۷ این
              شرکت به قیمتی معادل ۴۷۹ میلیون یورو به وسیلهٔ یک کنسرسیوم
              مشارکتی خریداری شد. این کنسرسیوم توسط دیوید ریچاردز مدیریت
              می‌شد. همچنین شرکت‌های کویتی الدار و سرمایه‌گذاری ادیم، همچنین
              بازرگان آمریکایی؛ جان سندرز در این کنسرسیوم، سرمایه‌گذاری کردند.
              شرکت فورد نیز با سرمایه ۷۷ میلیون دلار، مالکیت ۱۰٪ از شرکت استون
              مارتین را حفظ کرد.
            </p>
