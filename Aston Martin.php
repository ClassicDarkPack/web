              در لیگ فوتبال بوندسلیگای ۲ کشور آلمان فعالیت می‌کند و تیم دوم
              شهر مونیخ محسوب می‌شود. این باشگاه سالها در لیگ اول بوندسلیگا و
              تحت حمایت‌های مالی استون مارتین فعالیت می‌نمود
            </p>
