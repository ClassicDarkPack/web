            <p dir="rtl">
              در سال ۱۹۴۷ اولین خودروی جاده فراری با نام فراری ۱۲۵ اس طراحی و روانه بازار شد، که به دلیل طراحی مناسب و
              توان بالا موتور، مورد توجه قرار گرفت. در سال ۱۹۶۱ این شرکت با مشکلات اقتصادی شدیدی دست و پنجه نرم کرد،
              همچنین به رقابت با شرکت جگوار کارز پرداخت، که در نهایت با کمی کمک، فیات توانست از این مرحله با موفقیت عبور
              نماید. در فاصله سال‌های ۱۹۶۳ تا ۱۹۶۷ شرکت آمریکایی فورد با تولید سری فورد شلبی و فورد موستانگ تقریباً
              بسیاری از بازار خودروهای کلاس اسپورت را در دست گرفت. سپس فراری با طراحی مدل‌هایی همچون دینو، فراری جی‌تی و
              فراری جی‌تی‌او، توانست بازار را به نفع خود برگرداند. البته در طی این سال‌ها شرکت فورد با خودرو فورد
              جی‌تی۴۰ خود به ۶ سال قهرمانی پیاپی فراری در مسابقات لمانز پایان داد و تقریباً تمام کاپ‌ها را برد تا حتی
              تیم پورشه هم با بهترین خودروی خود یعنی پورشه ۹۱۷ نتواند کاری بکند. همچنین در سال‌های ۱۹۶۹ تا ۱۹۷۱، شرکت
              پورشه با مدل‌های ۹۰۸ و ۹۱۷ خود باز هم مسابقه‌ها را واگذار کرد، اما در یک مسابقه شرکت فراری با خودروی خود
              یعنی ۳۱۲پی توانست مسابقه را ببرد. در سال‌های ۱۹۷۲ و ۱۹۷۳ بازهم ۳۱۲پی چند افتخار دیگر برای فراری کسب کرد تا
              یکی از به یاد ماندنی‌ترین و بهترین تولیدات فراری باشد. از آن به بعد فراری از مسابقات لمانز کنار کشید و
              تمرکز خود را روی مسابقات فرمول ۱ متمرکز نمود. در ۱۹۸۸ انزو فراری درگذشت. در سال‌های ۱۹۹۷ و ۱۹۹۹، مایکل
              شوماخر به فراری پیوست و برای تیم فراری افتخارات زیادی کسب کرد. در سال‌های پیاپی این افتخارات ادامه داشت و
              در سال ۲۰۰۴ پایان یافت و مایکل شوماخر پس از ۲ سال افت شدید، از رانندگی در سال ۲۰۰۶ خداحافظی کرد.
