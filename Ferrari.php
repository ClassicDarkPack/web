              فِراری (به ایتالیایی: Ferrari) شرکت خودروسازی ایتالیایی است؛ که در زمینه طراحی و تولید خودروهای لوکس،
              اسپورت و سوپراسپورت فعالیت می‌نماید. پیشینه تأسیس شرکت فراری به سال ۱۹۲۹ و راه‌اندازی شرکت اسکودریا فراری،
              توسط انزو فراری بازمی‌گردد. این شرکت در سال‌های نخست، در زمینه طراحی و ساخت خودروهای مسابقه‌ای، همچنین
              حامی مالی مسابقات اتومبیل‌رانی فعالیت می‌کرد. این شرکت از سال ۱۹۴۷ تولید خودروهای جاده‌ای، با برند فراری
              را آغاز نمود. در سال ۱۹۶۹ شرکت خودروسازی فیات، اقدام به خریداری ۵۰٪ از سهام این شرکت نمود، که در سال ۲۰۰۸
              مالکیت فیات در فراری به ۸۵٪ افزایش پیدا کرد. هم‌اکنون شرکت اکسور با در اختیار داشتن ۲۲٪ و پیرو فراری
              (فرزند انزو) با ۱۰٪ از سهام این شرکت، مالکین اصلی فراری به‌شمار می‌آیند، همچنین بخشی از سهام این شرکت در
              بازار بورس نیویورک و بورس ایتالیا معامله می‌شود
