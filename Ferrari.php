            <p dir="rtl">

              در ۱۸ فوریه ۱۸۹۸ انزو آنسلمو فراری، در شهر مودنا کشور ایتالیا و در خانواده‌ای صنعتگر زاده شد. انزو در
              جوانی علاقه زیادی به تحصیلات رسمی و آکادمیک نداشت، ولی شیفته مسابقات اتومبیل‌رانی بود. او دوران سربازی را
              در جنگ جهانی اول آغاز نمود، ولی در حین خدمت در سال ۱۹۱۶ بر اثر شیوع بیماری آنفلوآنزا، به شدت بیمار و از
              سربازی معاف شد. در همان دوران، پدر وی نیز بر اثر همین بیماری درگذشت. بحران اقتصادی و مالی ناشی از جنگ،
              باعث شد تا شرکت خانوادگی آن‌ها ورشکسته شود و انزو فراری پس از تلاش ناموفق برای استخدام در کارخانه فیات، در
              شرکت کوچکی به‌نام سی‌ام‌ان که در زمینه تبدیل انواع کامیون و وانت به خودروهای سواری فعالیت داشت، مشغول به
              کار شد. فراری جوان، در سال ۱۹۱۹ با حضور در تیم اتومبیل‌رانی شرکت سی‌ام‌ان، وارد جهان مسابقات رالی شد، ولی
              موفقیت چندانی کسب نکرد. وی در سال ۱۹۲۰ شرکت سی‌ام‌ان را برای کار در شرکت آلفا رومئو ترک کرد و با تیم فرمول
              یک آلفا رومئو، توانست موفقیت قابل‌توجهی را در مسابقات محلی به دست آورد. فراری پس از کسب موفقیت‌های متعدد،
              با علم به اینکه موفقیت وی در مدیریت تیم، بیشتر از رانندگی حرفه‌ای است، در سال ۱۹۲۹ تیم اتومبیلرانی فراری
              را تأسیس کرد و از رانندگی خودروهای مسابقه‌ای، دست کشید. تیم فراری توانست با بهینه‌سازی خودروهای ساخت آلفا
              رومئو، به موفقیت چشمگیری دست یابد. انزو فراری از سال ۱۹۳۸ رسماً به عنوان مدیر واحد تولید خودروهای
              مسابقه‌ای شرکت آلفا رومئو منصوب گردید. هم‌زمان با جنگ جهانی دوم، دولت موسولینی شرکت آلفارومئو را مصادره
              نمود. البته تغییر چندانی در واحد سرپرستی فراری پدیدار نشد و تیم فراری که بر اساس قرارداد با آلفارومئو تا
              چهار سال اجازه شرکت در مسابقات را نداشت، موقتاً تغییر نام داد و مدتی به ساخت ابزارآلات و قطعات هواپیما
              مشغول بود. در طی این مدت خودروی مسابقه‌ای «تیپو ۸۱۵» را نیز عرضه کرد، که با توجه به محدودیت قانونی، در
              مسابقات، بسیار کم فروغ ظاهر شد. کارخانه کوچک فراری در سال ۱۹۴۳ به شهر مارانلو نقل مکان نمود. با وجود
              بمباران و تخریب کامل در سال ۱۹۴۴، کارخانه فراری از نو ساخته شد و فعالیت خود را در سال ۱۹۴۶ از سر گرفت. در
              همان سال، فراری تصمیم گرفت خط تولید خودروهای اسپورت غیرمسابقه‌ای را جهت کسب سود مالی لازم برای تأمین بودجه
              تیم فراری افتتاح نماید. در همین راستا، شرکت خودروسازی فراری در سال ۱۹۴۵ تأسیس گردید. اولین خودروی
              غیرمسابقه‌ای فراری در سال ۱۹۴۷ با نام فراری اس۱۲۵ عرضه شد. این خودرو مجهز به موتوری ۱۲ سیلندر بود، که فقط
              ۱۵۰۰ سی‌سی، حجم داشت. این خودرو در روز یازدهم ماه مه همان سال، توسط «فرانکو کورتزه» در پیست پیاچنزا آزمایش
              شد و دو هفته بعد، قهرمانی جایزه بزرگ رم را به دست آورد. دیگر خودروهای فراری سمبل زیبایی صنعت خودروسازی شده
              بودند که این امر حاصل به خدمت گرفتن طراحان و استودیوهای طراحی معروف ایتالیا بود. پس از به ثمر رسیدن
              سرمایه‌گذاری‌ها و سرازیر شدن درآمد حاصل از تولید خودروهای غیرمسابقه‌ای، درخشش خودروهای فراری در مسابقات
              جهانی آغاز شد. تیم فراری در سال ۱۹۵۰ در مسابقات فرمول یک شرکت کرد و اولین پیروزی خود را در سال ۱۹۵۱ در
              جریان مسابقه بزرگ انگلستان به دست آورد. سال بعد آلبرتو اسکاری راننده معروف آرژانتینی، اولین مدال قهرمانی
              جهان را، برای تیم فراری کسب کرد و بدین ترتیب، روند موفقیت فراری آغاز شد. فراری که قدیمی‌ترین تیم فرمول یک
              به حساب می‌آمد، تقریباً تمام رکوردهای فرمول یک، از جمله ۱۵ عنوان قهرمانی رانندگان و ۱۶ عنوان قهرمانی
              خودروسازان را به دست آورد. موفقیت این تیم مرهون جدیت و جسارت مردی بود که تا هنگام مرگ در سن ۹۰ سالگی
              همچنان مدیریت تیم خود را در دست داشت. آلفرد فراری پسر انزو، در سال ۱۹۵۶ در سن ۲۴ سالگی درگذشت و پس از مرگ
              او، انزو بیش از پیش گوشه‌گیر شد و تا پایان عمرش، در غم از دست دادن فرزندش، همواره عینک آفتابی به چشم داشت،
              ولی هیچ‌گاه علاقه به اتومبیل‌رانی را از دست نداد و تا آخرین لحظات مشغول اداره تیم خود بود. وی به دلیل
              مشکلات مالی، بالاخره در سال ۱۹۶۵ مجبور شد اندکی از سهام فراری را به شرکت فیات بفروشد. میزان سهام شرکت فیات
              در سال ۱۹۶۹ به ۵۰٪ افزایش یافت. در سال ۱۹۸۸ سهام شرکت تقسیم شد و فیات ۵۶٪، بانک ایتالیایی مدیوبانکا ۱۵٪،
              بانک آلمانی کومرزبانک ۱۰٪، برادران لیمن ۷٪ و پیرو فراری (پسرخوانده انزو) ۱۰٪ از سهام شرکت را در اختیار
              داشتند. در سال ۲۰۰۸ فیات سهام شرکاء، بجز پیرو فراری را خریداری کرد و میزان سهام آن به ۸۵٪ افزایش یافت. در
              حال حاضر کمپانی فیات ۹۰٪ از سهام فراری را در اختیار دارد و پیرو فراری نیز همچنان مالک ۱۰٪ از سهام شرکت
              می‌باشد. در ماه مه سال ۲۰۰۹ یک نمونه «تستا روسا» مدل ۱۹۵۷ فراری در مارانلو، ایتالیا به حراج گذاشته شد و به
              مبلغ ۱۲ میلیون دلار فروش رفت، که در آن زمان یک رکورد محسوب می‌شد. اکثر خودروهای فراری، امروزه به رنگ قرمز
              تولید می‌شوند، که این به پیشینه فراری در مسابقات و زمانی که فدراسیون اتومبیلرانی، قرمز را به عنوان رنگ
              کشور ایتالیا تعیین کرد، بازمی‌گردد. انزو فراری به پاس خدمات شایسته و کسب اعتبار برای کشورش به دریافت
              عالی‌ترین نشان‌های کشور ایتالیا؛ از جمله شوالیه و فرمانده نائل گشت و علاوه بر آن چندین لقب افتخاری در دیگر
              کشورها را نیز دریافت نمود. امروزه شرکت فراری علاوه بر تولید خودرو برای مسابقه و جاده، به شرکت تجاری
              بازرگانی بزرگی تبدیل شده، که دامنه متنوعی از محصولات شامل عینک، عطر، لباس، دوچرخه‌های پیشرفته، تلفن‌های
              موبایل،هدفون های جذاب و حتی کامپیوترهای همراه را به بازار عرضه نموده‌است. این شرکت با بیش از ۳۰ هزار
              کارمند، در سال ۲۰۱۱ درآمدی بالغ بر ۱۲ میلیارد یورو کسب کرد.</p>

