                        <h4> بررسی آستون مارتین ونکوئیش مدل ۲۰۱۵</h4>

                        <p>آستون‌مارتین ونکوئیش یک بهینه‌سازی متعادل برای مدل ۲۰۱۵ داشته و صاحب یک جفت نسبت انتقال قدرت
                            و چند تنظیم بهینه در سیستم دامپر شده است. البته چند آپشن رنگ‌آمیزی بدنه دیگر نیز به آن اضافه
                            شده‌اند. در سایر اجزا، این خودرو با همان شرایط قبلی باقی مانده است. درحالی‌که گران‌ترین
                            استون مارتین، دارای کیفیت‌های ستودنی زیادی است، امّا تازگی چیزی است که در این مدل دیده
                            نمی‌شود. این خودرو از همان ساختار VH استفاده می‌کند که از سال ۲۰۰۴ در محصولات این کمپانی به
                            کار رفته و همین‌طور برای پیشرانه از نسخه توسعه یافته همان موتور ۵/۹ لیتری V-12 بهره می‌برد.
                            البته خوشبختانه یک پلتفرم جدید و همچنین موتورهای V8 از AMG در راه هستند.
                        </p>
                        <div class="verticalhorizontal">
                            <img src="http://www.pedal.ir/wp-content/uploads/2015/02/2015-Aston-Martin-Vanquish-101-2.jpg"
                                alt="centered image" />
                        </div>
                        <strong>امّا واقعاً در کدام بخش‌های ونکوئیش نیاز به تغییر بوده است؟</strong>
                        <p> این خودرو در کلاسی قرار دارد که
                            رقیبانش همه جدیدتر و سریع‌تر هستند. ولی در همین حال، به عنوان خودرویی در حجم کم و به صورت
                            دستی ساخته می‌شود، سعی کرده که کاراکتر خود را حفظ کند. ونکوئیش همانند گذشته کار خود را به
                            عنوان یک «آستون‌مارتین» به‌خوبی انجام داده است؛ بزرگ، طراحی فروتنانه و اندکی حالت کلاسیک.
                            این خودرو به صاحبش نوعی خاص بودن، به معنای واقعی کلمه، می‌بخشد که نمی‌توان با هیچ قیمتی آن
                            را خرید. آستون‌مارتین ونکوئیش خودرویی است که باعث می‌شود یک لامبورگینی اونتادور، یک خودروی
                            معمولی جلوه کند و همین‌طور یک فراری F12 برلینتا یک انتخاب ساده تلقی شود.
                        </p>
                        <div class="verticalhorizontal">
                            <img src="http://www.pedal.ir/wp-content/uploads/2015/02/2015-Aston-Martin-Vanquish-105-2.jpg"
                                alt="centered image" />

                        </div>
                        <p>امّا در مورد تفاوت‌های مدل جدید صحبت کنیم. می‌توان گفت که تنها یک چیز در این خودرو عوض شده و
                            آن هم سیستم گیربکس اتوماتیک ۸ سرعته است. این سیستم در واقع همان گیربکس ZF است که در بسیاری
                            از مدل‌های اسپورت دیگر نیز استفاده شده است. امَا استون مارتین برای هماهنگ‌سازی آن، دچار
                            مشکلات و هزینه‌های مهندسی زیادی برای ونکوئیش شده است. با این که معمولاً اضافه‌شدن نسبت
                            انتقال قدرت برای بهینه‌سازی مصرف سوخت انجام می‌شود، ولی در اینجا برای افزایش عملکرد استفاده
                            شده است. ونکوئیش قدیمی توسط دنده‌ها به سرعت ۲۹۴/۵ کیلومتر بر ساعت محدود بود، ولی در حال حاضر
                            و بدون هیچ‌گونه تغییر مکانیکی دیگر، این خودرو می‌تواند به سرعت ۳۲۳/۴ کیلومتر بر ساعت برسد.
                            موضوعی که شاید برای خریداران ونکوئیش ۲۰۱۵ بیشتر از میزان برآورد مصرف سوخت آن اهمیت داشته
                            باشد.
                        </p>
                        </h4>
                        <div class="verticalhorizontal">
                            <img src="http://www.pedal.ir/wp-content/uploads/2015/02/2015-Aston-Martin-Vanquish-107-2.jpg"
                                alt="centered image" />
