                        <p>البته در تست انجام شده، سرعت ۳۲۳ کیلومتر بر ساعت از آن مشاهده نشد که چندان هم جای تعجب هم
                            ندارد. امَا در پیست، ونکوئیش چابکی خود را به نمایش گذاشته و نشان داده که در میان خودروهای
                            موجود در این کلاس خاص، چندان هم بی‌دست‌وپا نیست. این خودرو برای رسیدن به سرعت ۹۶/۶ کیلومتر
                            بر ساعت، زمان ۳/۶ ثانیه را ثبت کرده و همین‌طور در ۸/۳ ثانیه به سرعت ۱۶۰/۹ کیلومتر بر ساعت
                            رسیده است. در جاده، شرایط سیستم گیربکس با گذشته تفاوتی نکرده است و کارش را برای انتقال قدرت
                            موتور بسیار خوب انجام می‌دهد. در همین حال، با استفاده از دو پدل فلزی پشت فرمان می‌تواند به
                            صورت دستی دنده‌ها را کنترل کرد. البته با وجود تأخیر و درنگی که در تغییر دنده دیده می‌شود،
                            مشخص است که این خودرو هنوز هم با مشکل انتقال گشتاور و چرخ‌دنده‌های خورشیدی مواجه است.
                        </p>
                        <div class="verticalhorizontal">
                            <img src="http://www.pedal.ir/wp-content/uploads/2015/02/2015-Aston-Martin-Vanquish-102-2.jpg"
                                alt="centered image" />
