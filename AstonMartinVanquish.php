                        <p>موتور ۵/۹ لیتری V-12 نسبت به قبل هیچ‌گونه تغییری نکرده است. در حالی که این V12 نسبت به سایر
                            رقبای مدرن خود حرفی برای گفتن ندارد، ولی به عنوان یک پیشرانه، شخصیت خاص خود را دارد. در دور
                            موتور پایین شرایط ویژه یک V12 صدای زوزه نرم و ضعیفی دارد و بیشتر قیمت بالای خودرو را
                            می‌رساند. ولی در همین حال که دور موتور بالا می‌رود، صدا شدت بیشتری پیدا کرده و در بالاتر از
                            ۵۰۰۰ دور بر دقیقه به‌حداکثر هیجان خود می‌رسد.
                        </p>
                        <div class="verticalhorizontal">
                            <img src="http://www.pedal.ir/wp-content/uploads/2015/02/2015-Aston-Martin-Vanquish-104-2.jpg"
                                alt="centered image" />
