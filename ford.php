            <p dir="rtl">
              فورد ماستنگ (به انگلیسی: Ford Mustang) که با نام موستانگ نیز شناخته می‌شود، خودروی ساخته شده توسط کارخانه
              فورد است. نسل اول این خودرو بر روی پلتفرم نسل دوم فورد فالکون ساخته و در ۱۷ آوریل ۱۹۶۴ به بازار عرضه شد.
              در حال حاضر ششمین نسل از این مدل به بازار عرضه شده‌است. فورد ماستنگ کلاس تازه‌ای به نام پونی کار یا
              خودروهای پونی را در آمریکای شمالی ایجاد کرد؛ خودروهای اسپرت کوپه‌ای که کاپوتی کشیده و پشت کوتاهی داشتند.
              خودروهایی نظیر شورلت کامارو، ای‌ام‌سی جاولین و دوج چلنجر از دیگر ماشین‌ها در کلاس پونی کارز هستند. ماستنگ
              همچنین الهام بخش طراحی خودروهای اسپرت دیگری نظیر تویوتا سلیکا و فورد کاپری هم بود.
            </p>
            <p dir="rtl">
              فورد ماستنگ پنج ماه زودتر از آغاز رسمی ساخت خودروهای مدل ۱۹۶۵ به بازار عرضه شد. به همین خاطر به مدل‌هایی
              که در این پنج ماه تولید شد لقب «مدل ۱۹۶۴ و نیم» داده‌اند. ساخت رسمی خودرو از ۹ مارس ۱۹۶۴ در دیربورن در
              ایالت میشیگان آغاز شد. گفته می‌شود نام خودرو توسط جان نجار، طراح خودرو به خاطر علاقه‌اش به جنگنده‌های
              ماستنگ در جنگ جهانی دوم ماستنگ انتخاب شد. جان نجار در سال ۱۹۶۱ با همکاری فیلیپ تی کلاک در طراحی پروتوتایپی
              همکاری کرد که ماستنگ ۱ نام گرفت. ماستنگ ۱ به‌طور رسمی در ۷ اکتبر ۱۹۶۲ در گرندپری ایالات متحده آمریکا در
              واتکینس گلن در نیویورک رونمایی شد. دن گارنی، راننده فرمول یک آن را در پیست راند و رکوردش کمی بیشتر از
              ماشین‌های فرمول ۱ بود. روایت دیگری هم دربارهٔ انتخاب نام ماستنگ وجود دارد. گفته می‌شود رابرت جی اگرت، مدیر
              تحقیقات بازار فورد این نام را انتخاب کرد. براساس این روایت آقای اگرت کتاب «ماستنگ‌ها» نوشته جی. فرانک دوبی
              را که در سال ۱۹۶۰ چاپ شده بود از همسرش هدیه گرفت و بعداً همین نام الهام بخش نام کانسپت جدید فورد شد. او
              نام ماستنگ را به فهرست نام‌هایی که دربارهٔ آن بحث بود اضافه کرد و دست آخر این نام با فاصله زیاد انتخاب شد.
              امتیاز این نام در آلمان در اختیار شرکت کروپ بود و فورد از خرید این امتیاز با قیمت ده هزار دلار سرباز زد و
              این ماشین تا دسامبر ۱۹۷۸ با نام «تی-۵» در آلمان فروخته شد.</e>
            </p>
            <p dir="rtl">

              پروژه ساخت ماستنگ با کد تی-۵ ظرف ۱۸ ماه به مدیریت لی یاکوکا و سرمهندسی دونالد ان فری انجام شد. خودرویی
              که در این پروژه ساخته شد دو نفره روباز بود و از موتور چهار سیلندر خورجینی آلمانی فورد تانوس استفاده می‌کرد
              و در ظاهر بسیار شبیه پونتیاک فیه‌رو بود. ادعا می‌شود تصمیم برای ساخت یک ماشین دو نفره به دلیل فروش کم فورد
              تاندربرد مدل ۱۹۵۵ کنار گذاشته شد. برای بزرگتر کردن بازار این خودرو تصمیم گرفته شد ماشین با ردیف دوم صندلی
              ساخته شود که جای پای کمی داشت. برای افزایش جای داخل ماشین کمی از فضای صندوق هم کم شده بود.</p>

            </p>
            <p dir="rtl">
              نسل سوم ماستنگ براساس پلت فرم فاکس در سال ۱۹۷۳ ساخته شد. بدنه این نسل از نسل قبلی بزرگ‌تر ، فضای داخلی
              بیشتر و حجم موتور بیشتر بود. سبک‌های بدنه این نسل، هاچ‌بک و کروکی و ناچ‌بک بودند.تریم‌های موجود برای این
              نسل شامل یک مدل پایه (۱۹۷۹ - ۱۹۸۱)، کاروزریا گیا (۱۹۸۲ - ۱۹۹۳) ، توربوشارژ (۱۹۸۲ - ۱۹۹۳)، جی‌تی۲۰ (۱۹۸۲ -
              ۱۹۹۳)، اس‌وی‌او (فقط درسال ۱۹۸۴) و کبرا (۱۹۸۴ - ۱۹۸۶) بود. موتورهای این اتاق شامل ۲.۳ لیتری ۴ سیلندر، ۲.۸
              لیتری ۶ سیلندر و ۴.۹ لیتری ۸ سیلندر بودند. این نسل از ماستنگ یک فیس‌لیفت را برای سال ۱۹۸۷ یک فیس‌لیفت را
              دریافت کرد که نسخه فیس‌لیفت تا سال ۱۹۹۳ تولید شد
            </p>
