                            شرکت خودروسازی داج در سال ۱۹۰۰ توسط برادران داج به نام‌های جان فرانسیس داج و هوراس الجین
                            داج، در شهر دترویت، میشیگان تأسیس شد. این شرکت در سال‌های نخست فعالیت خود، قطعات مختلف
                            خودرو را برای شرکت‌های فورد و اولدزموبیل تولید می‌نمود. نخستین خودروی ساخت این شرکت، با
