                            داج در سال‌های ۱۹۶۸ تا ۱۹۷۴ خودروهایی در کلاس «عضلانی» تولید کرد. یک خودروی عضلانی در
                            کامل‌ترین تعریف یک خودروی سایز متوسط، چهار نفره، دارای توانایی‌هایی حرکتی در حد مشخص، مجهز
                            به یک موتور وی-۸ بلوک بزرگ و قیمت مناسب می‌باشد، به طوری که قابل خریداری توسط بیشتر افراد
                            جامعه بوده و مثل بسیاری از خودروهای اسپرت منحصر به قشر خاصی از جامعه نباشد.
                            یک نمونه عضلانی از گونه کلاسیک
                            تعریف دقیق تنها شامل خودروهای سایز متوسط می‌شود. در واقع تا سال ۱۹۶۴ از مدل‌های سایز متوسط
                            خبری نبود. پیش از آن سازندگان با افزایش قدرت و توانایی‌های مدل‌های فول سایز، آن‌ها را تبدیل
                            به عضلانی می‌کردند. به همین خاطر نخست خودروهای فول سایز تقویت شده توسط شرکت داج، عضلانی
                            نامیده می‌شدند.
                            علاوه بر مدل‌های فول سایز و سایز متوسط، مدل‌های کوچکی هم بودند که توانایی‌های خود را به رخ
                            بقیه می‌کشیدند. این مدل‌های جدید به اصطلاح پونی کارز نام گرفتند. (که به معنی اسب کوچک
                            می‌باشد) این خودروها تنها در صورتی جزو دستهٔ عضلانی قرار می‌گیرند که توانایی‌های آن‌ها در
                            بالاترین حد ممکن و در حد عضلانی‌های واقعی باشد.
