                            این کمپانی در طول تاریخ فعالیت خود در مسابقات جهانی متعددی حضور داشته است. آئودی در مسابقات
                            رالی، فرمول یک و Le Mans موفق به کسب پیروزی‌های قابل توجهی گشته و توانسته نام خود را به
                            عنوان یکی از تیم‌های قابل توجه در تاریخ این مسابقات جاودانه کند. از خودروهای موفق این شرکت
                            در حال حاضر می‌توان به ماشین‌های خانوادگی A1 و A3، ماشین‌های لوکس A4 تا A8 و ماشین‌های اسپرت
                            TT و R8 اشاره کرد.
                        </p>
