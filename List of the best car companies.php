                            حوزه باز کند. با این حال حتی خلاقانه‌ترین ماشین‌های دنیا در رقابت با نام‌های شناخته شده‌ای
                            چون فراری و فورد شانس چندانی ندارند. از طرف دیگر نبرد اقتصادی بین چین و آمریکا در سال گذشته
