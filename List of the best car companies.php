                        <div class="verticalhorizontal">
                            <img src="https://www.plaza.ir/wp-content/uploads/2020/03/BMW.jpg" alt="centered image" />
                        </div>
                        <p>کمپانی بی ام دبلیو را می‌توان یکی از بهترین شرکت های خودروسازی دنیا دانست. بیش از 104 سال از
                            تاسیس این شرکت می‌گذرد. حوزه فعالیت‌های این کمپانی به شدت گسترده بوده و مواردی چون قایق‌های
                            تفریحی، موتور هواپیما و موتورسیکلت را شامل می‌شود.
                            <br>
                            بی ام دبلیو ماشین‌های خود را تحت سه برند مختلف عرضه می‌کند. این سه برند رولز رویس، بی ام
                            دبلیو و مینی هستند. علاوه بر این موتورسیکلت‌های این شرکت تحت عنوان Motorrad به فروش می‌روند.
                            این شرکت در سال 2015 به عنوان دوازدهمین تولیدکننده بزرگ وسایل نقلیه انتخاب شد. این کمپانی
                            تاکنون موفقیت نسبتا خوبی در زمینه مسابقات حرفه‌ای کسب کرده است. از محبوب‌ترین ماشین‌های
                            کنونی بی ام دبلیو می‌توان به سری‌های 1 تا 8 اشاره کرد. علاوه بر این بی ام دبلیو حضور فعالی
                            در حوزه ماشین‌های الکتریکی و اسپرت دارد.
                        </p>
