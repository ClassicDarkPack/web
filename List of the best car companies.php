                                alt="centered image" />
                        </div>
                        <p>یکی دیگر از شرکت های خودروسازی مشهور انگلیسی، استون مارتین است. این شرکت در سال 1913 توسط
                            لیونل مارتین و رابرت بمفورد شروع به کار کرد. نام این کمپانی با ماشین‌های گران‌قیمت سری
                            فیلم‌های جیمز باند گره خورده است. ماشین‌های اسپورت استون مارتین یکی از مشهورترین نمادهای
                            فرهنگی انگلستان به حساب می‌آیند. این کمپانی از اوایل دهه 80 میلادی به عنوان تامین‌کننده
                            اختصاصی خودروهای شاهزاده‌های ولز انتخاب شده است.
                            <br>
                            استون مارتین در بیش از 50 کشور دنیا حضوری فعال داشته و به عنوان یکی از مشهورترین برندهای
                            تاریخ شناخته می‌شود. لازم به ذکر است که این کمپانی در طول دوران فعالیت خود هفت بار اعلام
                            ورشکستگی کرده است. این شرکت علاوه بر تولید خودرو در حوزه‌های ساخت قایق، دوچرخه، لباس، منازل
                            مسکونی، زیردریایی و هواپیما نیز فعالیت دارد. استون مارتین یکی از پایه‌های ثابت مسابقات Le
                            Mans بوده و تاکنون موفق به کسب سه پیروزی در آن‌ها شده است.
                        </p>
