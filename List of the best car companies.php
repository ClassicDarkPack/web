                        <p>یکی از بزرگ‌ترین طراحان ماشین قرن گذشته اتوره بوگاتی است. او در سال 1909 کمپانی خودروسازی
                            شخصی خود به نام بوگاتی را در فرانسه احداث کرد. ماشین‌های بوگاتی بیش از هر چیزی با زیبایی و
                            قدرت قابل توجه خود در مسابقات جهانی شناخته می‌شوند. از مشهورترین مدل‌های مسابقه‌ این کمپانی
                            می‌توان به Type 35 Grand Prix، مدل Type 41 Royale، مدل Type 57 Atlantic و Type 55 اشاره کرد.
                            <br>
                            با مرگ اتوره بوگاتی و فرزندش، کار این شرکت تمام شده به نظر می‌رسید. آن‌ها تا پیش از دهه 50
                            میلادی موفق به فروش 8000 ماشین شده بودند. در دهه 60 بخش ساخت قطعات هواپیمای بوگاتی به فروش
                            رفت. خواب زمستانی غول فرانسوی تا دهه 90 ادامه داشت. در این زمان یک طراح ایتالیایی آن را به
                            عنوان برندی برای تولید ماشین‌های محدود و خاص احیا کرد. در حال حاضر گروه فولکس‌واگن مالک برند
                            بوگاتی است. از مشهورترین مدل‌های بوگاتی می‌توان به ویرون و شیرون اشاره کرد که موفقیت بی‌نظیر
                            آن‌ها بوگاتی را به یکی از بزرگترین شرکت های خودروسازی جهان تبدیل کرده است.
                        </p>
