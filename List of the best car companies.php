                            تبدیل کمپانی او به یکی از بزرگ‌ترین شرکت های خودروسازی آمریکا و جهان شده است. کمپانی فورد در
                            سال 1903 ثبت گشت. این شرکت ماشین‌های لوکس خود را تحت نام لینکولن به فروش می‌رساند. فورد
                            همچنین مالک کارخانه برزیلی ترولر، هشت درصد کمپانی استون مارتین و 32 درصد شرکت چینی Jiangling
                            Motors است.
                            <br>
                            فورد زیرشاخه‌های متعددی در بسیاری از کشورهای جهان چون تایوان، ترکیه و روسیه دارد. این شرکت
                            را می‌توان پایه‌گذار بسیاری از سیاست‌های کلان در صنعت خودروسازی به حساب آورد. امروزه این
                            قواعد به نام فوردیسم شناخته شده و در دستور کار شمار زیادی از کارخانه‌های صنعتی دنیا قرار
                            دارند. جالب است بدانید فورد در مقاطعی مالک شرکت‌های مشهوری چون ولوو بوده است. این کمپانی در
                            جریان بحران مالی قرن بیست و یکم تا مرز ورشکستگی پیش رفت. فورد از فعال‌ترین تیم‌های حاضر در
                            مسابقات مختلف اتوموبیل‌رانی بوده و افتخارات متعددی در این حوزه کسب کرده است.

                        </p>
                        <h4>4- بی ام دبلیو
