                            این شرکت‌ها، آئودی است. این کمپانی عضوی از مجموعه فولکس‌واگن محسوب می‌شود. نخستین آجرهای
                            شرکت آئودی توسط مهندسی به نام آگوست هورچ در همکاری با شرکت‌های DKW و Wanderer نهاده شدند.
                            نخستین ماشین‌های ساخته شده توسط این شرکت آلمانی در عصر مدرن سری Audi F103 بودند. چهار حلقه
                            به تصویر کشیده شده در لوگوی آئودی نماینده اتحاد چهار پایه‌گذار آن هستند. مدیران آئودی از
                            ابتدای تاسیس، هدف خود را «پیش افتادن از طریق فناوری» عنوان کرده‌اند.
