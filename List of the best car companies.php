                        <h4>10- جگوار
                        </h4>
                        <div class="verticalhorizontal">
                            <img src="https://www.plaza.ir/wp-content/uploads/2020/03/jaguar.jpg"
                                alt="centered image" />
                        </div>
                        <p>اگر از طرفداران برنامه‌های تلویزیونی The Grand Tour و Top Gear باشید، یقینا از علاقه جرمی
                            کلارکسون و به طور کلی انگلیسی‌ها به ماشین‌های جگوار خبر دارید. این کمپانی بریتانیایی کار خود
                            را در سال 1922 و با تولید موتورسیکلت‌های سایدکار آغاز کرد. نخستین ساخته این شرکت انگلیسی یک
                            ماشین چهار در دو و نیم لیتری به نام SS Jaguar بود.
                            <br>
                            شرکت جگوار با تولید مجموعه‌ای از ماشین‌های اسپورت چشم‌نواز چون Jaguar XK120 و E-Type به شهرت
                            رسید. شعار جگوار در این سال‌ها «شکوه، فضا، سرعت» بود. شاید بتوان بزرگ‌ترین نقطه قوت این
                            کمپانی را نبوغ مهندسان آن دانست. آن‌ها موفق به خلق موتوری کم‌مصرف و در عین حال کوچک و
                            کاربردی شده بودند. در حال حاضر محبوب‌ترین محصولات این کمپانی را ماشین‌های لوکسی چون Jaguar
                            XJ و XF و شاسی‌بلندهایی چون F-Pace تشکیل می‌دهند. علاوه بر این جگوار با مدل I-Pace ورودی
                            موفقیت‌آمیز به حوزه ماشین‌های برقی داشت. این کمپانی بدون شک یکی از بزرگترین شرکت های
                            خودروسازی جهان است.
                        </p>
