                        <p>انزو فراری از شناخته‌شده‌ترین نام‌ها در حوزه شرکت های خودروسازی جهان است. این طراح مشهور در
                            سال 1939 کمپانی اختصاصی خود را تاسیس کرد. او در ابتدا تنها بر ماشین‌های مسابقه تمرکز داشت و
                            نخستین ماشین خود را در سال 1940 تولید کرد. فراری یکی از موفق‌ترین شرکت‌ها در زمینه مسابقات
                            جهانی است.
                            <br>
                            فراری در سال 2014 به عنوان قدرتمندترین برند دنیا شناخته شد. مدل 250 GTO این کمپانی، ساخته
                            سال 1964، با قیمت 70 میلیون دلاری لقب گران‌ترین ماشین تاریخ را کسب کرده است. از محبوب‌ترین
                            ماشین‌های کنونی فراری می‌توان به GTC4Lusso، مدل F8، مدل Portofino و SF90 Strandale اشاره
                            کرد. این ماشین‌ها به عنوان سمبلی از سرعت و ثروت در نظر گرفته می‌شوند.
                        </p>
