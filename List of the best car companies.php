                            بسیاری نخستین پورشه تولید شده را مدل 356 می‌دانند. این ماشین توسط فرزند فردیناند پورشه، فری،
                            ساخته شد. فری در ادامه، مدیریت کمپانی پدر فقید خود را بر عهده گرفت. در حال حاضر پورشه به
                            عنوان یکی از موفق‌ترین کمپانی‌های آلمان به حساب می‌آید. از مدل‌های کنونی این شرکت می‌توان به
                            ماشین‌های اسپورت Boxstar Roadstar و 911 اشاره کرد. علاوه بر این پورشه در حوزه ماشین‌های
                            خانوادگی نیز فعال بوده و موفقیت مدل سدان Panamera سندی بر آن است. این کمپانی یکی از
                            موفق‌ترین تیم‌ها در مسابقات Le Mans را در اختیار داشته و تاکنون موفق به کسب 19 پیروزی در آن
                            شده است.
                        </p>
