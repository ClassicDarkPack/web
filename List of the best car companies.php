                        <p>یکی از شناخته‌شده‌ترین نام‌های حوزه خودروسازی بنز است. این شرکت آلمانی در حال حاضر زیرشاخه‌ای
                            از کمپانی دایملر بوده و بیش از هر چیز با ماشین‌های لوکس، ون، وانت، اتوبوس و آمبولانس‌های خود
                            شناخته می‌شود. این شرکت در سال 1926 شروع به کار کرد و شعار آن «بهترین یا هیچ» است.
                            <br>
                            از محبوب‌ترین مدل‌های کنونی مرسدس بنز می‌توان به ماشین‌های لوکس سری A، سری B، سری C، سری E،
                            سری S و سری‌های اسپورت SLC Class و GLA Class اشاره کرد. ماشین‌های این شرکت با ماندگاری و
                            کیفیت بالای خود شناخته می‌شوند. بنز کارخانه‌های متعددی در بسیاری از کشورهای دنیا چون
                            آرژانتین و اسپانیا دارد. این کمپانی را می‌توان از موفق‌ترین شرکت های خودروسازی جهان در زمینه
                            مسابقات جهانی چون فرمول یک و Le Mans دانست.
                        </p>
                        <h4>2- لامبورگینی
                        </h4>
                        <div class="verticalhorizontal">
                            <img src="https://www.plaza.ir/wp-content/uploads/2020/03/Lamborghini.jpg"
                                alt="centered image" />
                        </div>
                        <p>طراحان ایتالیایی صنعت خودروسازی بیش از هر چیزی با ظرافت کار خود شناخته می‌شوند. ظرافت و
                            زیبایی را می‌توان برجسته‌ترین ویژگی‌های یک لامبورگینی دانست. این شرکت کار خود را از سال 1963
                            میلادی به رهبری فروچو لامبورگینی آغاز کرد. لامبورگینی در آن زمان با هدف رقابت با فراری تصمیم
                            به استفاده از موتورهای وسط عقب-محور عقب گرفت.
                            <br>
                            این کمپانی در طول تاریخ خود فراز و نشیب‌های متعددی را از سر گذرانده است. محبوب‌ترین
                            ماشین‌های این شرکت خودروسازی در حال حاضر اونتیدور (Aventador)، اوراکان (Huracan) و شاسی‌بلند
                            اوروس (Urus) هستند. علاوه بر این نباید از مواردی چون گالاردو غافل شد. تیم لامبورگینی تاکنون
                            در 80 مسابقه فرمول یک شرکت کرده و برخلاف انتظارات موفقیت چندانی کسب نکرده است.
                        </p>
                        <h4>1- فراری
                        </h4>
                        <div class="verticalhorizontal">
