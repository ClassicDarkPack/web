              تویوتا یک شرکت معمولی تولید خودرو نیست. از همان آغاز راه‌اندازی شرکت، اصول و مبانی بکر مدیریتی در شرکت به
              کار گرفته شد. این همان وقتی بود، که کیشیرو تویودا جوان در کارخانه ریسندگی پدرش ساکیشی تویودا، با ایده‌گیری
              استفاده از نوار نقاله، خط مونتاژ تولید موتور گازوئیلی کوچکی را، با کمک مهندسان جوان دیگر راه‌انداخت.

              پدر کیشیرو معتقد بود، هیچ فرایندی را نمی‌توان کامل نامید و همیشه جایی برای بهبود وجود دارد و همین اندیشه
              بهبود بود، که فرایند کایزن یا بهبود مستمر را در تویوتا نهادینه کرد.

              ماجرای تولد شرکت جدید تولید خودرو در گوشه کوچکی از کارخانه ریسندگی پدر، اندیشه ساده و جمع و جور کار کردن و
              بی‌ریخت و پاش بودن و آراستگی محیط کار را، در ذهن و دل کارکنان شرکت جدید، جای داد و بدین ترتیب شرکت
              خودروسازی تویوتا، مدل سامانه تولیدی خاص خود را، به دنیا عرضه کرد.

              بنیان سامانه تولید تویوتا، فلسفه حذف کامل اتلاف‌هاست! کیشیرو معتقد بود، شرایط ایده‌آل برای تولید وقتی است،
              که ماشین‌آلات و افراد با هم کار کنند، تا بدون هیچ‌گونه اتلافی ارزش افزوده ایجاد شود.

              اونوتایشی، مهندس جوانی که در سال ۱۹۳۲ به تویوتا پیوست در توسعه اندیشه کایزن، در تویوتا نقش مهمی داشت. او
              با ابداع سامانه کانبان، بُعد دیگری از سامانه تولید تویوتا را عرضه کرد و با دمینگ و ایشیکاوا همکاری نزدیکی
              داشت.

              تایچی اونو، پیشتاز سامانه تولید تویوتا در دهه ۱۹۵۰، بر این باور بود، که بزرگ‌ترین مشکل وقتی است، که
              هیچگونه مشکلی وجود نداشته باشد. او به‌طور مستمر به کارکنان می‌گفت به هر موضوعی که برخورد می‌کنید، پنج بار
              بپرسید «چرا؟» مثالی که خود او می‌زد؛ یک روبات جوشکاری بود، که در وسط کار متوقف می‌شود و با این پنج پرسش،
              ریشه مشکل به دست می‌آید.

              چرا روبوت متوقف شد؟
              زیرا مدار آن بیش از حد بار کشید و فیوز سوخت.
              چرا مدار بیش از حد بار کشید؟
              زیرا یاتاقان‌ها به خوبی روانکاری نشده بودند.
              چرا یاتاقان‌ها خوب روانکاری نشده بودند؟
              زیرا گردش پمپ روغن ناقص صورت می‌گیرد.
              چرا گردش روغن به خوبی صورت نمی‌گیرد؟
              زیرا خروجی آن با آشغال‌های فلزی مسدود شده‌است.
              چرا خروجی آن مسدود شده‌است؟
              زیرا فیلتری روی پمپ وجود ندارد.
              بدین ترتیب پاسخ مشکل پیدا می‌شود. گرچه ابداع‌گر اصل پنج چرا، ساکیشی تویودا بود، ولی اونو این اصل را توسعه
              داد. اونو معتقد بود، علت ریشه‌ای مشکلات را کشف کردن، خود عامل و کلید حل مشکلات بعدی است… داده‌ها در تولید
              مهم است، اما واقعیت، مهم‌تر است. پس باید عمیق نگاه کرد و به ریشه رسید.
