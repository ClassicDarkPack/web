              درآمد شرکت تویوتا در سال ۲۰۱۲ معادل ۱۹۷٫۲۹ میلیارد دلار و سود خالص آن ۳ میلیارد دلار بوده‌است. هم‌اکنون
              تویوتا پس از گروه فولکس‌واگن با درآمد ۲۴۷٫۷۷ میلیارد دلار و سود خالص ۲۸٫۱۳ و جنرال موتورز با درآمد ۱۵۰٫۲۷۶
              میلیارد دلار و سود خالص ۷٫۵۸۵ میلیارد دلار سومین خودروساز برتر جهانی و نخستین خودروساز ژاپن محسوب می‌شود.

              رشد روزافزون تویوتا به گونه‌ای است، که به زودی رقیب دوم خود را پشت سر خواهد گذاشت و دور نیست، که با عقب
              گذاشتن جنرال موتورز به غول برتر خودروسازی جهان تبدیل شود.

              تویوتا در سراسر ژاپن ۱۲ کارخانه تولیدی دارد. بازار خارجی تویوتا ۱۷۰ کشور و منطقه را در برمی گیرد. شرکت‌های
              تولیدی خارجی سازنده تویوتا ۵۲ شرکت و در ۲۷ کشور جهان پراکنده می‌باشند. در سال ۲۰۰۵، در ژاپن ۶۵۷۹۸ نفر در
              کارخانه‌های شرکت کار می‌کرده‌اند، که با احتساب ۵۲۳ شرکت زنجیره تأمین، که ۲۳۱ شرکت آن در خارج از ژاپن مستقر
              است، این تعداد به ۲۸۵۹۷۷ نفر می‌رسد.

              در سال ۲۰۰۵، تویوتا ۸۱۲ میلیارد ین در زمینه تحقیق و توسعه هزینه کرده‌است. آخرین دستاورد فناوری تویوتا،
              تولید خودروی هیبریدی سینرژی است، متشکل از دو منبع قدرت: موتور الکتریکی و موتور گازی / بنزینی. در این
              فناوری امکان صرفه‌جویی در مصرف سوخت، صدای کمتر و رانندگی بهتر، تعبیه شده‌است.
