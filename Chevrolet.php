                            نام خودروهای این شرکت، از نام راننده موفق سوئیسی؛ لوئی شور.له گرفته شده‌است. او در سال ۱۸۷۸
                            در کشور سوئیس از پدر و مادر فرانسوی زاده شد و سپس در آنجا به کار در یک شرکت مورس پرداخته و
                            سپس با برادران خود، بنام‌های آرتور و گاستون تصمیم گرفت به کانادا مهاجرت کند.
                            <br>
                            وی سپس وارد ایالات متحده گردید. در شهر نیویورک امکان کار بر روی خودروها و کامیون‌های کارخانه
                            ویلیام والتر برای لوئیس فراهم آمد. او سپس شروع به کار در کارخانه‌های پرشمار کرد و توانست خود
                            را به‌عنوان یکی از بهترین رانندگان مسابقات نشان دهد و همین امر باعث شد، تا توجه ویلیام سی.
                            دورانت - مؤسس شرکت جنرال موتورز - به وی جلب شده و دورانت او را به عنوان راننده جنرال موتورز
                            استخدام کند.
                            <br>
                            لوئی شوروله و ویلیام دورانت، خودرو جدیدی به سبک خودروهای فرانسوی طراحی نموده و نام آن را
                            شوروله گذاشتند. در سال ۱۹۱۱ نمونه کلاسیک و شش سیلندر این مدل برای عرضه به بازار ایالات
                            متحده، تولید شد. در سوم نوامبر آن سال، کارخانه شورلت موتور تأسیس شد و ۲٫۹۹۹ دستگاه از این
                            خودرو را، در همان سال تولید کرد.
                            <br>
                            شرکت میسون موتور که پیش‌تر تحت نظارت دورانت بود، به شورلت واگذار گردید و در سال ۱۹۱۳ این دو
                            شرکت در هم ادغام شدند. در ۱۹۱۳ خودرو چهار سیلندر بی‌بی در دو مدل یک سرنشینه و مدل سلطنتی، در
                            رنگ‌های آبی و سفید تولید شد. طرح رنگ آمیزی این خودرو، از روی کاغذ دیواری هتلی که دورانت در
                            فرانسه در آن اقامت داشت، الهام گرفته شده بود.
                            <br>
                            در سال ۱۹۱۳ لوئی شوروله و دو نفر دیگر، بنام‌های وی. آر هفلر و جی‌بویز تصمیم گرفتند، تا
                            کارخانه تولید موتورهای هوایی شورلت را تأسیس کنند. اما این کارخانه در همان آغاز کار، ورشکست
                            گردید و در سال ۱۹۴۱، لوئیس شورلت به صورتی مرموز، کشته شد و راز قتل او در پرده‌ای از ابهام
                            باقی‌ماند.
                            <br>
                            در سال ۱۹۱۴ نام شرکت به مکس‌ول موتور تغییر کرد و در شهر نیویورک تعداد ۵٫۰۰۵ دستگاه خودرو
                            تولید شد و در همین سال، مدل دیگری نیز به نام شورلت ۴۹۰ و با قیمت ۴۹۰ دلار به بازار عرضه شد.
                            <br>
                            طی دو سال شمار ۷۰٫۷۰۱ دستگاه از این خودرو به فروش رفت و در سال ۱۹۱۷ اولین خودرو اسپورت بدون
                            سقف این شرکت نیز ساخته شد. در ۱۹۱۸ شورلت شروع به تولیدات تجاری مانند کامیون کرد.
                            <br>
                            در ۱۹۱۹ شرکت شورلت به دو بخش شرکت دورانت و بخش جنرال موتورز تقسیم شد و تا قبل از جنگ جهانی
                            دوم، معادل ۱۴۹٫۹۰۴ دستگاه خودرو تولید نمود.
                            <br>
                            در ۱۹۲۱ یعنی پس از جنگ به علت کمبود سرمایه پنج میلیونی، شرکت شورلت تا مرز سقوط و ورشکستگی
                            پیش رفت، اما پیش از آن در سال ۱۹۱۱ نیز با این وضع مواجه شده بود، که با مدیریت خوب ویلیام
                            دورانت در مدتی کمتر از ده سال، به یکی از بزرگ‌ترین کارخانجات خودروسازی جهان تبدیل گشت.
                            <br>
                            یکی از بزرگ‌ترین مشکلات شرکت، در ۱۹۲۳ برگشت خوردن موتورهای هواپیمای تولیدی شورلت بود، که
                            ضرری بزرگ به شورلت وارد ساخت. در سال ۱۹۲۷ خودروی جدید شورلت، توانست از نظر تعداد تولیدات
                            مقام نخست را، در صنعت، به خود اختصاص دهد. این وسیله نقلیه به تعداد یک میلیون و هزار و هشتصد
                            و هشتاد دستگاه تولید شد.
                            <br>
                            در ۱۹۲۹ طرح جدیدی از یک خودروی شش سیلندر و طرحی چهار سیلندر از همین خودرو، به منظور کاهش
                            قیمت، ارائه و از آن به تعداد ۱٫۳۲۸٫۶۰۵ دستگاه تولید شد.
                            <br>
                            در سال ۱۹۳۵ شورلت، تولید ده میلیونمین خودرو خود را جشن گرفت. در ۱۹۳۶ سامانه ترمزهای شورلت به
                            صورت هیدرولیک درآمد. در ۱۹۳۹ شرکت با تولید ۱۵ میلیون‌امین خودروی خود، شورلت سایتیشن را به
                            بازار عرضه کرد.
                            <br>
                            قبل از آغاز جنگ جهانی دوم، شرکت رکورد فروش خودرو در سال را، شکسته و در ۱۹۴۱ رقمی معادل ۱٫۴
                            میلیون فروش را، در بازارهای جهانی خودرو، به نام خود ثبت کرد.
                            <br>
                            در آن سال، یکی از خودروهای کوپه شرکت نیز، در چند مسابقه اتومبیل‌رانی، موفق به کسب مقام شد.
                            در سال ۱۹۴۸ دبلیو. اف آرمسترانگ، یکی از مدیران موفق جنرال موتورز سرپرستی شورلت را به عهده
                            گرفت.
                            <br>
                            با ورود این مدیر جدید، سبک تولید خودروها، در کارخانجات شورلت دوباره تغییر و مدل‌های دیگری از
                            آن، را در ۱۹۵۰ به بازار عرضه کرد. بر اثر این سبک جدید، غیر از تغییرات در بدنه خودرو، تغییرات
                            عمده‌ای در ساختار موتور و همچنین قیمت آن، حاصل شد.
                            <br>
                            از سال ۱۹۵۰ به بعد، بیشتر مدل‌های شورلت، با گیربکس تمام خودکار بودند. یکی از کارهای مهم
                            آرمسترانگ، تولید مدل‌های بسیار ارزان قیمت بود، که در رشد اقتصادی شرکت شورلت اهمیت بسیار
                            داشت.
                            <br>
                            همچنین در ۱۹۵۰ مهندس هارلی جی. ایرل، مدل شورلت کوروت را، طراحی و تولید کرد، که در سال ۱۹۵۹
                            در مدل‌های شورلت کودیاک و شورلت ایمپالا، به بازار عرضه شدند.
                            <br>
                            علی‌رغم اینکه این دو مدل، نتوانستند مانند خودروهای مطرح این شرکت، فروش بالایی داشته باشند،
                            اما با این حال تا سال ۱۹۶۹ تولید آن‌ها ادامه داشت.
                            <br>
                            در ۱۹۶۲ شورلت نوا به عنوان مدلی برگرفته از شورلت کورن‌ویر و ایمپالا طراحی و ساخته شد، در
                            ۱۹۶۴ مدلی بزرگتر از آن، به بازار عرضه گردید. همچنین در همان سال، شورلت کاپریس با قیمت ۳٫۳۴۷
                            دلار در کنار شورلت نوا که قیمتی معادل ۲٫۰۲۸ دلار داشت، به معرض فروش گذاشته شد.[۴] در سال
                            ۱۹۶۳ شرکت شورلت آنچنان پیشرفت کرده بود، که از هر ۱۰ خودروی تولید شده در ایالات متحده، یک
                            خودرو از تولیدات شورلت اختصاص داشت.
                            <br>
                            در سال ۱۹۶۷ مدلی اسپورت و کوپه بنام شورلت کامارو ساخته و تا دو دهه، به‌عنوان یکی از
                            مطرح‌ترین خودروهای اسپورت، که نظر تمام حادثه جویان را به خود جلب کرده بود، تولید می‌شد.
                            <br>
                            شورلت بلیزر کی۵، تولید دهه ۱۹۷۰
                            یکی از مطرح‌ترین مدل‌های کامارو مدل زد، با حجم موتور ۵۷۰۰ سی‌سی، که با موتور کوروت، در سال
                            ۱۹۸۹ به بازار عرضه شد. در دهه ۱۹۷۰، از موتورهای شرکت شورلت، توسط بسیاری از کارخانه‌های دیگر
                            مانند اوپل و اولدزموبیل، که موتور خودروهای شش و هشت سیلندر خود را، از این شرکت خریداری
                            می‌کردند، استفاده می‌شد.
                            <br>
                            در آغاز دهه ۱۹۷۰ قراردادی بین دو شرکت شورلت و ایسوزو منعقد و بنابر آن مقرر شد، که شورلت تمام
                            تجهیزات خودروسازی این کارخانه را، تأمین نماید.
                            <br>
                            شورلت در سال ۱۹۷۱ مدل‌های شورلت نوا، شورلت مونت کارلو، شورلت بل ایر، شورلت بلیزر و شورلت
                            سابربن را، به مرحله تولید رساند و در همین سال، مدل لوکسی از کاپریس نیز، به بازار عرضه کرد،
                            که فروش فوق‌العاده‌ای را برای این شرکت رقم زد.
                            <br>
                            در سال ۱۹۷۵، مدل کاوالیر، یکی از موفق‌ترین خودروهای این شرکت، طراحی و تولید گردید. در میانه
                            دهه ۱۹۸۰ نیز این شرکت روابط تجاری خود را با خودروسازهای ژاپنی مانند سوزوکی و تویوتا گسترش
                            داد. اکنون این شرکت به عنوان یکی از بزرگترین تولیدکنندگان خودروی جهان، مطرح است.
